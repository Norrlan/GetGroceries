package com.example.getgroceries;
// model class for nutrition
public class NutritionModel
{
    Integer calories;

    String protein;
    String carbs;

    String fats;

    //getters
    public Integer getCalories()
    {
        return calories;
    }

    public String getProtein()
    {
        return protein;
    }

    public String getCarbs()
    {
        return carbs;
    }

    public String getFats()
    {
        return fats;
    }
}
